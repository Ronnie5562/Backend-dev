class Calc {
    // A calculator class

    public int add(int num1, int num2) {
        return (num1 + num2);
    }

    public int sub(int num1, int num2) {
        return (num1 - num2);
    }

    public int mul(int num1, int num2) {
        return (num1 * num2);
    }

    public float div(int num1, int num2) {
        return (num1 / num2);
    }
    
    public int mod(int num1, int num2) {
        return (num1 + num2);
    }
}

